def fetch_data(ticker):
    try:
        data = yf.download(ticker, period="5d", interval="5m", progress=False)
        return data
    except Exception as e:
        print(f"Error fetching {ticker}: {e}")
        return None

def calculate_macd(data):
    exp1 = data["Close"].ewm(span=12, adjust=False).mean()
    exp2 = data["Close"].ewm(span=26, adjust=False).mean()
    macd = exp1 - exp2
    signal = macd.ewm(span=9, adjust=False).mean()
    return macd, signal

def check_breakouts(tickers):
    for ticker in tickers:
        data = fetch_data(ticker)
        if data is None or len(data) < 35:
            continue
        vwap = (data["Close"] * data["Volume"]).cumsum() / data["Volume"].cumsum()
        macd, signal = calculate_macd(data)

        macd_cross = macd.iloc[-1] > signal.iloc[-1]
        vwap_reclaim = False
        if isinstance(vwap, pd.Series):
            vwap_reclaim = data["Close"].iloc[-1] > vwap.iloc[-1]

        if macd_cross and vwap_reclaim:
            send_pushover_notification(f"ðŸ“ˆ Breakout Alert: {ticker} â€” MACD Bullish & VWAP Reclaimed")

